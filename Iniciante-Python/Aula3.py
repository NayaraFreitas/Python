"""
Python = Linguagem de programaçãp
Tipo de tipagem = Dinâmica (ja sabe o tipo {string. number,...})/ forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas Simples '' 
print('Hello World')
print('Hello "World"') # maisa simples se precisar usar aspas detro do texto


# Aspas duplas ""
print("Hello World")
print("Hello 'World'") # maisa simples se precisar usar aspas detro do texto


# Escape
print("Hello \"World\" ")

# r - expressão regulares
print(r"Hello \"World\" ")

